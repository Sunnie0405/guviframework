
public class Variableanddatatypes {
	
	public static void main(String[] args) {
		
		int a = 12;
		String s = "Hello";		
		boolean d = true;
		char k = 'A';
		int[] arrInteger = {1,2,3,4,5,6,7};
		System.out.println(arrInteger.length);
		System.out.println(arrInteger[5]);
		System.out.println(arrInteger[arrInteger.length-2]);
		System.out.println(a);
		System.out.println(s);
		System.out.println(d);
		System.out.println(k);
		System.out.println(arrInteger[arrInteger.length-5]+55);
		
	}

}
